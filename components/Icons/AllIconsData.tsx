export default function AllIcons({
  allIconsState,
  setAllIconsState,
}: {
  allIconsState: IconData[];
  setAllIconsState: React.Dispatch<React.SetStateAction<IconData[]>>;
}) {
  function handleClickedIcon(singleIcon: IconData) {
    setAllIconsState((prevState) =>
      prevState.map((icon) => ({
        ...icon,
        isSelected: icon.id === singleIcon.id ? !icon.isSelected : false,
      }))
    );
  }

  return (
    <div className="flex flex-wrap gap-2 text-[#abe1f7] p-3">
      {allIconsState.map((singleIcon, index) => (
        <div
          key={index}
          onClick={() => handleClickedIcon(singleIcon)}
          className={`w-9 h-9 shadow-sm cursor-pointer border border-slate-50 flex items-center justify-center rounded-lg hover:bg-[#42a8d1] hover:text-white ${
            singleIcon.isSelected
              ? "bg-[#1b6a88] text-white"
              : "bg-white text-[#1b6a88]"
          }`}
        >
          {singleIcon.icon}
        </div>
      ))}
    </div>
  );
}
